#include "3-calc.h"

/**
 * op_add - function addition
 * @a: num1
 * @b: num2
 * Return: return sum of a and b
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - function of subtraction
 * @a: num1
 * @b: num2
 * Return: Subtraction of num1 and num2
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_div - function of division
 * @a: num1
 * @b: num2
 * Return: division of num1 and num2
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mul - function of multiplication
 * @a: num1
 * @b: num2
 * Return: mulitplication of num1 and num2
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_mod - function of remainder
 * @a: num1
 * @b: num2
 * Return: remainder of num1 and num2
 */

int op_mod(int a, int b)
{
return (a % b);
}
